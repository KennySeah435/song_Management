@app.route("/songs", methods=["POST"])
def add_song():
    title = (request.form.get("title") or "").strip()
    if not title:
        return jsonify({"error": "Title is required"}), 400

    row = {
        "title": title,
        "title_zh": (request.form.get("title_zh") or "").strip(),
        "key": (request.form.get("key") or "").strip(),
        "beats": (request.form.get("beats") or "").strip(),
        "chord_sheet": None,
        "chord_sheet_path": None,
        "lead_sheet": None,
        "lead_sheet_path": None,
        "jianpu": None,
        "jianpu_path": None,
        "audio": None,
        "audio_path": None,
        "youtube_url": (request.form.get("youtube_url") or "").strip(),
        "remark": (request.form.get("remark") or "").strip(),
        "created_at": datetime.now().strftime("%Y-%m-%d %H:%M"),
    }

    created = insert_song(row)
    song_id = created["id"]

    updates = {}
    for field in FILE_FIELDS:
        uploaded = save_upload(request.files.get(field), field, song_id)
        if uploaded:
            updates[field] = uploaded["url"]
            updates[PATH_FIELDS[field]] = uploaded["pathname"]

    if updates:
        created.update(updates)
        created = update_song_row(song_id, created)

    return jsonify(created), 201


@app.route("/songs/<int:song_id>", methods=["PUT"])
def update_song(song_id):
    existing = song_exists(song_id)
    if not existing:
        return jsonify({"error": "Song not found"}), 404

    title = (request.form.get("title") or "").strip()
    if not title:
        return jsonify({"error": "Title is required"}), 400

    row = {
        "title": title,
        "title_zh": (request.form.get("title_zh") or "").strip(),
        "key": (request.form.get("key") or "").strip(),
        "beats": (request.form.get("beats") or "").strip(),
        "youtube_url": (request.form.get("youtube_url") or "").strip(),
        "remark": (request.form.get("remark") or "").strip(),
        "chord_sheet": existing.get("chord_sheet"),
        "chord_sheet_path": existing.get("chord_sheet_path"),
        "lead_sheet": existing.get("lead_sheet"),
        "lead_sheet_path": existing.get("lead_sheet_path"),
        "jianpu": existing.get("jianpu"),
        "jianpu_path": existing.get("jianpu_path"),
        "audio": existing.get("audio"),
        "audio_path": existing.get("audio_path"),
    }

    for field in FILE_FIELDS:
        path_field = PATH_FIELDS[field]
        new_file = request.files.get(field)
        if new_file and new_file.filename:
            remove_upload(existing.get(field), existing.get(path_field))
            uploaded = save_upload(new_file, field, song_id)
            row[field] = uploaded["url"] if uploaded else None
            row[path_field] = uploaded["pathname"] if uploaded else None
        elif request.form.get(f"clear_{field}") == "1":
            remove_upload(existing.get(field), existing.get(path_field))
            row[field] = None
            row[path_field] = None

    updated = update_song_row(song_id, row)
    return jsonify(updated)


@app.route("/songs/<int:song_id>", methods=["DELETE"])
def delete_song(song_id):
    existing = song_exists(song_id)
    if not existing:
        return jsonify({"error": "Song not found"}), 404

    for field in FILE_FIELDS:
        remove_upload(existing.get(field), existing.get(PATH_FIELDS[field]))

    delete_song_row(song_id)
    return jsonify({"deleted": song_id})


